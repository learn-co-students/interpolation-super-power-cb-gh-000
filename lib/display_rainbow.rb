colors=["red","orange","yellow","green","blue","indigo","violet"]
def display_rainbow(colorarr)
  print ("R: #{colorarr[0]},")
  print (" O: #{colorarr[1]},")
  print (" Y: #{colorarr[2]},")
  print (" G: #{colorarr[3]},")
  print (" B: #{colorarr[4]},")
  print (" I: #{colorarr[5]},")
  print (" V: #{colorarr[6]}\n")
end
display_rainbow(colors)
