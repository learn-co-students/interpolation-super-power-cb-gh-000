def display_rainbow(arr)
  print "#{arr[0][0].upcase}: #{arr[0]}, "
  print "#{arr[1][0].upcase}: #{arr[1]}, "
  print "#{arr[2][0].upcase}: #{arr[2]}, "
  print "#{arr[3][0].upcase}: #{arr[3]}, "
  print "#{arr[4][0].upcase}: #{arr[4]}, "
  print "#{arr[5][0].upcase}: #{arr[5]}, "
  print "#{arr[6][0].upcase}: #{arr[6]}\n"
end
