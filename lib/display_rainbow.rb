def display_rainbow(colors)
    roygbiv = ["R","O","Y","G","B","I","V"]
    print "#{roygbiv[0]}: #{colors[0]}, "
    print "#{roygbiv[1]}: #{colors[1]}, "
    print "#{roygbiv[2]}: #{colors[2]}, "
    print "#{roygbiv[3]}: #{colors[3]}, "
    print "#{roygbiv[4]}: #{colors[4]}, "
    print "#{roygbiv[5]}: #{colors[5]}, "
    puts "#{roygbiv[6]}: #{colors[6]}"
end
