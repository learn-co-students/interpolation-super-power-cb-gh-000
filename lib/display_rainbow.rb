def display_rainbow(colors)
  string = ""
  string += "R: #{colors[0]}, "
  string += "O: #{colors[1]}, "
  string += "Y: #{colors[2]}, "
  string += "G: #{colors[3]}, "
  string += "B: #{colors[4]}, "
  string += "I: #{colors[5]}, "
  string += "V: #{colors[6]}"
  puts string
end