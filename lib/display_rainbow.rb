def display_rainbow(colours)
  print "#{colours[0][0].upcase}: #{colours[0]}, "
  print "#{colours[1][0].upcase}: #{colours[1]}, "
  print "#{colours[2][0].upcase}: #{colours[2]}, "
  print "#{colours[3][0].upcase}: #{colours[3]}, "
  print "#{colours[4][0].upcase}: #{colours[4]}, "
  print "#{colours[5][0].upcase}: #{colours[5]}, "
  print "#{colours[6][0].upcase}: #{colours[6]}\n"
end
